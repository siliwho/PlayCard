void StartDefaultTask(void const * argument)
{
  menu_init();

  for(;;)
  {
    AppID app = menu_run();
    osDelay(50);

    if (app == APP_SNAKE) {
        // snake_run();
    }
    else if (app == APP_PONG) {
        int score = pong_run();
        (void)score;
    }
    else if (app == APP_TODO) {
        todo_run();
    }
    else if (app == APP_IMAGE) {
        image_viewer_run();
    }
    else {
        osDelay(10);
    }

    osDelay(1);
  }
}


